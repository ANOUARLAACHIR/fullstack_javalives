package com.codewa.live02;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        //in java we have 8 primitive data types
        //char, byte, short, int, long, float, double and boolean
        //characters simple quote (')
        char questionMark = '?';
        //integers
        byte age = 33;
        short year = 2024;
        int salary = 2_003_003;
        long population = 8_100_000_00L;
        //floating point numbers
        double currency = 10.33;
        float weight = 70.9F;
        //boolean
        boolean isRaining = true;
 }
}
